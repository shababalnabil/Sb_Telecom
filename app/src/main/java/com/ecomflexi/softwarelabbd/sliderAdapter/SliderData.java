package com.ecomflexi.softwarelabbd.sliderAdapter;

public class SliderData {

    private String sliderimg, sliderText, sliderUrl, Only;

    public String getSliderimg() {
        return sliderimg;
    }

    public void setSliderimg(String sliderimg) {
        this.sliderimg = sliderimg;
    }

    public String getSliderText() {
        return sliderText;
    }

    public void setSliderText(String sliderText) {
        this.sliderText = sliderText;
    }

    public String getSliderUrl() {
        return sliderUrl;
    }

    public void setSliderUrl(String sliderUrl) {
        this.sliderUrl = sliderUrl;
    }

    public String getOnly() {
        return Only;
    }

    public void setOnly(String only) {
        Only = only;
    }
}
